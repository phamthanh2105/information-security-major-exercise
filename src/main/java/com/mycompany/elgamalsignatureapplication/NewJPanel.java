    private boolean verifySignature(String message, String signature) throws Exception {
        String[] parts = signature.split(",");
        BigInteger r = new BigInteger(parts[0], 16);
        BigInteger s = new BigInteger(parts[1], 16);

        // Hash the message
        MessageDigest md = MessageDigest.getInstance("SHA-256");
        byte[] messageHash = md.digest(message.getBytes());
        BigInteger hash = new BigInteger(1, messageHash);

        BigInteger p = elParams.getP();
        BigInteger g = elParams.getG();
        BigInteger y = ((ElGamalPublicKeyParameters) keyPair.getPublic()).getY();

        // Validate signature
        BigInteger pMinusOne = p.subtract(BigInteger.ONE);
        if (r.compareTo(BigInteger.ONE) < 0 || r.compareTo(pMinusOne) >= 0) {
            return false;
        }
        BigInteger v1 = g.modPow(hash, p);
        BigInteger v2 = (y.modPow(r, p).multiply(r.modPow(s, p))).mod(p);
        return v1.equals(v2);
    }

