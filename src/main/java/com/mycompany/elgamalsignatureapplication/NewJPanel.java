    private AsymmetricCipherKeyPair generateElGamalKeyPair() {
        SecureRandom random = new SecureRandom();
        BigInteger p = new BigInteger("E95E4A5F737059DC60DFC7AD95B3D8139515620F", 16);
        BigInteger g = new BigInteger("B4E68DABC1CE0E6DB72AD5E1F3E750919C9B3277", 16);
        elParams = new ElGamalParameters(p, g);
        ElGamalKeyGenerationParameters keyGenParams = new ElGamalKeyGenerationParameters(random,
                elParams);

        ElGamalKeyPairGenerator keyPairGen = new ElGamalKeyPairGenerator();
        keyPairGen.init(keyGenParams);

        return keyPairGen.generateKeyPair();
    }

    private String signMessage(String message) throws Exception {
        String hashHex = hashMessage(message);
        jTextField2.setText("Message Hash: " + hashHex);

        // Generate a random k
        SecureRandom random = new SecureRandom();
        BigInteger k;
        BigInteger p = elParams.getP();
        BigInteger pMinusOne = p.subtract(BigInteger.ONE);
        do {
            k = new BigInteger(p.bitLength(), random);
        } while (k.compareTo(BigInteger.ONE) <= 0 || k.compareTo(pMinusOne)
                >= 0 || !k.gcd(pMinusOne).equals(BigInteger.ONE));

        // Calculate r and s
        BigInteger g = elParams.getG();
        BigInteger r = g.modPow(k, p);
        BigInteger x = ((ElGamalPrivateKeyParameters) keyPair.getPrivate()).getX();
        BigInteger kInverse = k.modInverse(pMinusOne);
        BigInteger s = (new BigInteger(1, new BigInteger
        (hashHex, 16).toByteArray()).subtract(x.multiply(r)).
                multiply(kInverse)).mod(pMinusOne);

        // Combine r and s into a single signature string
        String signature = r.toString(16) + "," + s.toString(16);

        return "Signature: " + signature;
    }

    private String hashMessage(String message) throws Exception {
        MessageDigest md = MessageDigest.getInstance("SHA-256");
        byte[] messageHash = md.digest(message.getBytes());
        return new BigInteger(1, messageHash).toString(16);
    }
